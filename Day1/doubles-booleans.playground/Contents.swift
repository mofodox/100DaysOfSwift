import UIKit

// Double is short for "double-precision floating point number"
// This is how to create fractional values such as 38.1, 3.141592654
// Doubles are different than Int, and you cannot mix them

var pi = 3.141 // This is a type of Double
type(of: pi)

// Booleans are much simpler. It is just true or false. Swift will automatically assign the
// boolean type to any variable assigned either true or false value.

var awesome = true
type(of: awesome)

