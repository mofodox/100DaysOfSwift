import UIKit

// To create a variable in Swift, we use the var keyword
var str = "Hello, playground"

// In Swift, variables are mutable it means that the value can be change
// With this example, we use the variable str, assigned a new value which is a string = "Goodbye"
str = "Goodbye"

// Here we create a new variable called name and assign the variable a type of string = "Kai"
var name = "Kai"

// Here we use the same variable name and change the variable name with a new value = "mofodox"
name = "mofodox"
